

public class Ifelse01 {
    public static void main(String[] args) {
        int money = 5000;
        if (money >= 8000) {
            System.out.println("순대국 냠냠");
        } else if(money >= 5000) {
            System.out.println("잔치국수 냠냠");
        } else {
            System.out.println("흑흑 배고프다.");
        }
    }
}
